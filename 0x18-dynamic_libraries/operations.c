#include "main.h"

/**
 * add - adds two integers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiply two integers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two integers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
